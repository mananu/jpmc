//extends Calculator
class ScientificCalculator extends Calculator{
	
	//returns the sine of num1
	sin(num1){
		return Math.sin(num1); 
	}	
	
	//returns the cosine of num1
	cos(num1){
		return Math.cos(num1); 
	}
	
	//returns the tangent of num1
	tan(num1){
		return Math.tan(num1); 
	}
	
	//returns the logarithm of num1
	log(num1){
		return Math.log(num1); 
	}
}